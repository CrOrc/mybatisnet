(
   ACCOUNT_ID                     int                            not null,
   ACCOUNT_FIRSTNAME              varchar(32)                    not null,
   ACCOUNT_LASTNAME               varchar(32)                    not null,
   ACCOUNT_EMAIL                  varchar(128),
   primary key (ACCOUNT_ID)
